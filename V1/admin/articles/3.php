Bonjour,
Depuis peu le site City-mix ( www.city-mix.com ) est opérationnel
<br><br>
A quoi ça sert ?
<br>

Trouver tous les commerces autour de vous par géolocalisation, classé du plus proche au plus lointain.
Filtre de recherche: Par villes, par catégories, par distances et (en développement) par services « accepte la carte bleue, parking… »
<br>Est ce gratuit ?
<br><br>
L’inscription et le référencement de commerces est gratuit. Nous souhaitons qu’un maximum de personnes participent au projet et aide au développement du site. L’idée est basé sur le côté pratique de trouver TOUT ce qu’il est possible de faire à proximité.
 <br>
<br>
Fonctionnement ?
<br>
Les visiteurs du site, comme les professionnels, peuvent s’inscrire gratuitement et référencer les commerces de leur choix, qu’ils en soient propriétaire ou non.
<br>Les propriétaires de commerces peuvent gratuitement revendiquer la propriété d’un commerce et récupérer la gestion de la fiche.
<br>Les utilisateurs peuvent commenter et noter si un commerce leur à plus ou non<br>
<br>RECRUTEMENT:
<br>City-mix recherche des volontaires aidant au développement du site.
<br>
Nous recherchons des volontaires pour ajouter des commerces, voir gérer une ville et s’occuper de développer la notoriété du site, ainsi que développer son utilité dans les villes qui ne sont pas encore référencés.
<br>
